const fs = require('fs')
const resolve = require('path').resolve
const join = require('path').join
const findup = require('findup')

const nodeModulesPath = join(process.cwd(), '..').replace(/\\/g, '\\\\')

exports.generatedMessage = 'Generated by ghooks. Do not edit this file.'
exports.nodeBin = getNodeBin(nodeModulesPath)

exports.content = fs
  .readFileSync(resolve(`${__dirname}/hook.template.raw`), 'UTF-8')
  .replace('{{generated_message}}', exports.generatedMessage)
  .replace('{{node_bin}}', exports.nodeBin)
  .replace('{{node_modules_path}}', nodeModulesPath)

function getNodeBin(path) {
  try {
    const pkgJsonPath = join(findup.sync(path, 'package.json'), 'package.json')
    const pkgJson = JSON.parse(fs.readFileSync(pkgJsonPath, 'utf-8'))

    return pkgJson.config.ghooks.node
  } catch (e) {
    return 'node'
  }
}
